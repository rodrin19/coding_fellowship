let person = {  //declare variable and tell js it's an object by using an object literal
     //in between curly braces we place properties (key:value pairs)
     name: 'Naomi',  //name is the key. Naomi is the value together they are a property of the person object
     age: '19',
     height: 5,
}
console.log(person["height"]);
//I can access values of an object using dot notation objectName.proptertyName